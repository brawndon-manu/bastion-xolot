def lookup_vendor(mac: str) -> str:
    """
    Return the manufacturer name for a MAC address, or 'Unknown' if not found.

    Accepts any common MAC format:
      aa:bb:cc:dd:ee:ff
      AA-BB-CC-DD-EE-FF
      aabbccddeeff
    """
    if not mac:
        return "Unknown"

    normalized = mac.upper().replace("-", ":").replace(".", ":")

    # Strip any separators and reformat cleanly
    digits = normalized.replace(":", "")
    if len(digits) < 6:
        return "Unknown"

    oui = f"{digits[0:2]}:{digits[2:4]}:{digits[4:6]}"
    return _OUI_TABLE.get(oui, "Unknown")
